### Lưu ý:
1. **Đóng dấu nháy kép**: Đảm bảo rằng tất cả các chuỗi được đóng đúng cách.
2. **Sử dụng `eval` cẩn thận**: `eval` có thể thực thi mã từ các chuỗi, vì vậy hãy chắc chắn rằng các biến của bạn không chứa mã độc hại.

### Chạy tập lệnh từ GitHub:
Nếu bạn muốn chạy tập lệnh từ GitHub, hãy đảm bảo rằng nội dung của tập lệnh trên GitHub là chính xác và không có lỗi cú pháp. Bạn có thể tải xuống và kiểm tra nội dung của tập lệnh trước khi chạy nó:

```bash
# Tải xuống tập lệnh
curl -O https://raw.githubusercontent.com/mbohon1/Rocky-Linux-8-x64/main/ipv6.sh

# Kiểm tra nội dung tập lệnh
cat ipv6.sh

# Chạy tập lệnh
bash ipv6.sh
```

Nếu bạn vẫn gặp lỗi, vui lòng cung cấp thêm thông tin chi tiết về lỗi hoặc mục tiêu cụ thể của tập lệnh để tôi có thể giúp bạn tốt hơn.

